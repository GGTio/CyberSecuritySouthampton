    <td width="50%" valign="top"><h2 name="intro">Introduction</h2>
    <p>This module is undertaken by most of the MSc cohorts in Electronics and Computer Science. The Project Preparation Module will prepare students for the summer Masters Project. It will give students a grounding in the research  methods and techniques they will need in order for them to plan and  successfully execute and complete their project. </p>
    <p>Part of the Module involves identifying potential  areas of research, negotiating with the Programme Leader and potential supervisors.  This will involve general  research and the review of literature and the identification of a research  question or questions, culminating in the submission of the General Research Review.  They will then  develop a research plan which will be presented in summary form via a submitted  coursework, the Project Plan and a Poster presentation.  This  will be presented via a Module Poster Exhibition at the end of the semester and peer assessed.</p>
    <p>It  is expected that as part of this module the students will undertake appropriate  preparatory study for their summer project, e.g. learning specific new  analytical, simulation or other technical skills where necessary.  The module also teaches students what it is to  be a professional practitioner, examining ethical and legal issues around  professional practice. </p>
    <h2 name="sup_assignments">Supervisor Allocations</h2>
    <p>Preliminary allocation of students to supervisors will be performed by the Programme Leaders over the first two weeks of term through organised sessions. The Programme Leader can assist in general literature reading to start familiarising yourself with general thematic elements and the basics of given areas. NOTE: at this point you may not have finally decided on an area of study. You will then have two weeks to meet with, discuss and if necessary arrange reassigment in determining the area for your summer project. Also NOTE that supervisors will be very busy during this period with marking of Semester 1 material and Semester 2 teaching. It is YOUR responsibility to arrange any meetings. </p>
    <h2><a href="coursework1.php">Coursework 1: General Research Review</a></h2>
    <p>You will  write a short three page General Research Review, details of which can be found on a supplementary page<a href="coursework1.php"> here</a>. During this period you should arrange to meet with your supervisor a couple of times to discuss the material. This will be marked by your supervisor with moderation and a detailed examination of any plagiarism or AI issues, as discussed in lectures.</p>
    <h2><a href="coursework2.php">Coursework 2: Project Plan</a></h2>
    <p>Following the review, you will then prepare a five page Project Plan according to the details and structure found <a href="coursework2.php">here</a>. This will be marked by your supervisor and moderated by a second examiner.</p>
    <h2><a href="coursework3.php">Coursework 3: Poster</a></h2>
    <p>At the same time, you should prepare a poster detailing your project idea, aims and methods.  Summary information is presented <a href="ELEC6211 Poster Coursework.pdf">here</a> and detailed information will be provided in one of the Friday Lectures. The poster will be presented at a student conference at the end of the examination period. This will be peer assessed and marked by an academic. </p>
